    /**
     * .Subscribe to a particular Website
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function sub(Request $request,Website $website)
    {

       $subMail = $request->validate([
            'email' => 'email:rfc,dns',
        ]);

        $subscriber = Subscriber::firstOrCreate(['email' => $subMail]);

        return $subscriber->websites()->syncWithoutDetaching([$website->id]);
    }

